// Task 4: Calculating area and volume
 
// Task 4.1: Area of a circle
const radius = 5;
const circleArea = Math.PI * radius ** 2;
console.log("Area of a circle:", circleArea.toFixed(2));
 
// Task 4.2: Area of a rectangle
const length = 8;
const width = 4;
const rectangleArea = length * width;
console.log("Area of a rectangle:", rectangleArea.toFixed(2));
 
// Task 4.3: Volume of a cylinder
const cylinderRadius = 3;
const height = 10;
const cylinderVolume = Math.PI * cylinderRadius ** 2 * height;
console.log("Volume of a cylinder:", cylinderVolume.toFixed(2));